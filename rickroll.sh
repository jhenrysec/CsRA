rickroll() {
    clear
    echo -e "\033[1;31m                  ██████╗ ██╗ ██████╗ ██╗  ██╗    ██████╗  ██████╗ ██╗     ██╗     \033[0m"
    echo -e "\033[1;31m                  ██╔══██╗██║██╔═══██╗██║ ██╔╝    ██╔══██╗██╔═══██╗██║     ██║     \033[0m"
    echo -e "\033[1;31m                  ██████╔╝██║██║   ██║█████╔╝     ██████╔╝██║   ██║██║     ██║     \033[0m"
    echo -e "\033[1;31m                  ██╔══██╗██║██║   ██║██╔═██╗     ██╔══██╗██║   ██║██║     ██║     \033[0m"
    echo -e "\033[1;31m                  ██║  ██║██║╚██████╔╝██║  ██╗    ██║  ██║╚██████╔╝███████╗███████╗\033[0m"
    echo -e "\033[1;31m                  ╚═╝  ╚═╝╚═╝ ╚═════╝ ╚═╝  ╚═╝    ╚═╝  ╚═╝ ╚═════╝ ╚══════╝╚══════╝\033[0m\n"

    local idx=0
    local lyric_idx=0
    local lyrics=(
        "We're no strangers to love"
        "You know the rules and so do I"
        "A full commitment's what I'm thinking of"
        "You wouldn't get this from any other guy"
        "I just wanna tell you how I'm feeling"
        "Gotta make you understand"
        "Never gonna give you up"
        "Never gonna let you down"
        "Never gonna run around and desert you"
        "Never gonna make you cry"
        "Never gonna say goodbye"
        "Never gonna tell a lie and hurt you"
    )

    trap 'tput cnorm; echo -e "\n\033[1;32mThanks for the endless party! 🎉\033[0m"; exit 0' INT
    tput civis  # Hide cursor

    while true; do
        # Move to top and print next panel
        printf "\033[3;1H\033[J"
        echo "${panels[$idx]}"

        idx=$(( (idx + 1) % ${#panels[@]} ))

        # Lyrics at bottom
        printf "\033[$(($(tput lines) - 12));1H"
        echo -e "\033[1;36m${lyrics[$lyric_idx]}\033[0m"

        lyric_idx=$(( (lyric_idx + 1) % ${#lyrics[@]} ))

        sleep 0.45   # Animation speed - adjust if needed
    done
}

rickroll
